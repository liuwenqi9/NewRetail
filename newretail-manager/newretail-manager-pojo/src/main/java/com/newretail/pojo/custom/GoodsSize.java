package com.newretail.pojo.custom;

public class GoodsSize {
	
	private String sizeName;
	
	private String sizePrice;

	public String getSizeName() {
		return sizeName;
	}

	public void setSizeName(String sizeName) {
		this.sizeName = sizeName;
	}

	public String getSizePrice() {
		return sizePrice;
	}

	public void setSizePrice(String sizePrice) {
		this.sizePrice = sizePrice;
	}

	@Override
	public String toString() {
		return "GoodsSize [sizeName=" + sizeName + ", sizePrice=" + sizePrice + "]";
	}

}
