package com.newretail.pojo.custom;

public class MerchantActivities {

    private String attribute;
    private String description;
    private String icon_color;
    private String icon_name;
    private int id;
    private boolean is_exclusive_with_food_activity;
    private String name;
    private String tips;
    private int type;
    public void setAttribute(String attribute) {
         this.attribute = attribute;
     }
     public String getAttribute() {
         return attribute;
     }

    public void setDescription(String description) {
         this.description = description;
     }
     public String getDescription() {
         return description;
     }

    public void setIcon_color(String icon_color) {
         this.icon_color = icon_color;
     }
     public String getIcon_color() {
         return icon_color;
     }

    public void setIcon_name(String icon_name) {
         this.icon_name = icon_name;
     }
     public String getIcon_name() {
         return icon_name;
     }

    public void setId(int id) {
         this.id = id;
     }
     public int getId() {
         return id;
     }

    public void setIs_exclusive_with_food_activity(boolean is_exclusive_with_food_activity) {
         this.is_exclusive_with_food_activity = is_exclusive_with_food_activity;
     }
     public boolean getIs_exclusive_with_food_activity() {
         return is_exclusive_with_food_activity;
     }

    public void setName(String name) {
         this.name = name;
     }
     public String getName() {
         return name;
     }

    public void setTips(String tips) {
         this.tips = tips;
     }
     public String getTips() {
         return tips;
     }

    public void setType(int type) {
         this.type = type;
     }
     public int getType() {
         return type;
     }

}